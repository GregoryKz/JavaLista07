package dev_klaus.lista07;

/*
 * Exercicio 070 - Ímpares entre 0 e 20
 * Apresentar os valores inteiros ímpares na faixa de 0 a 20.
 * Feito por Klaus
 */

public class Exercicio070_Impares {
    public static void main(String[] args) {
        for (int i = 1; i <= 20; i += 2) {
            System.out.print(i + " ");
        }
    }
}
